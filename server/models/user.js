import mongoose from "mongoose";
const userSchema = new mongoose.Schema({

    name:{
        type:String,
        required:true
    },

    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    role: {
        type: String,
        enum: ["patient", "doctor"],
        default: "patient"
    }

})

export default mongoose.model("User", userSchema);